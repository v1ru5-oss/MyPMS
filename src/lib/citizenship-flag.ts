export function citizenshipFlagEmoji(name: string): string {
  return NAME_TO_FLAG[name.trim()] ?? ''
}

/** ISO-код страны или null («Иное», неизвестное имя). */
export function citizenshipIsoCode(name: string): string | null {
  return NAME_TO_ISO[name.trim()] ?? null
}

/** Текст опции селекта: флаг + пробел + название (если флаг есть). */
export function citizenshipOptionLabel(name: string): string {
  const flag = citizenshipFlagEmoji(name)
  return flag ? `${flag}\u00A0${name}` : name
}
